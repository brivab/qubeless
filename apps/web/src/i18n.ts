function getStoredLocale(): Locale {
  if (typeof window === 'undefined') return fallbackLocale;
  try {
    const stored = window.localStorage.getItem(STORAGE_KEY);
    if (stored === 'en' || stored === 'fr') return stored;
  } catch {
    // ignore
  }
  return fallbackLocale;
}

const locale = ref<Locale>(getStoredLocale());
if (typeof document !== 'undefined') {
  document.documentElement.lang = locale.value;
}

function interpolate(template: string, params?: Record<string, string | number>) {
  if (!params) return template;
  return template.replace(/\{(\w+)\}/g, (match, key) => {
    const value = params[key];
    return value === undefined ? match : String(value);
  });
}

function t(key: string, params?: Record<string, string | number>) {
  const table = messages[locale.value] ?? messages[fallbackLocale];
  const fallback = messages[fallbackLocale];
  const template = table[key] ?? fallback[key] ?? key;
  return interpolate(template, params);
}

function setLocale(next: Locale) {
  if (next === locale.value) return;
  locale.value = next;
  if (typeof document !== 'undefined') {
    document.documentElement.lang = next;
  }
  if (typeof window !== 'undefined') {
    try {
      window.localStorage.setItem(STORAGE_KEY, next);
    } catch {
      // ignore
    }
  }
}

function getLocaleTag(value?: Locale) {
  const next = value ?? locale.value;
  return next === 'fr' ? 'fr-FR' : 'en-US';
}

export function useI18n() {
  return {
    locale,
    setLocale,
    t,
    getLocaleTag,
  };
}
